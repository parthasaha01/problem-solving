#include<bits/stdc++.h>
using namespace std;
int main()
{
    int tt; scanf("%d",&tt);

    for(int ks=1; ks<=tt; ks++)
    {
        int n; scanf("%d",&n);

        for(int i=1; i<n; i++)
        {
            int u,v; scanf("%d%d",&u,&v);
        }

        if(n==1)
        {
            printf("Case #%d: 1\n",ks);
        }
        else
        {
            printf("Case #%d: 2\n",ks);
        }
    }

    return 0;
}
