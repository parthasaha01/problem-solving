#include<bits/stdc++.h>
using namespace std;
int main()
{
    int tt; scanf("%d",&tt);

    for(int ks=1; ks<=tt; ks++)
    {
        int n,m; scanf("%d%d",&n,&m);

        for(int i=1; i<=m; i++){
            int u,v; scanf("%d%d",&u,&v);
        }

        if(n&1)printf("no\n");
        else printf("yes\n");
    }
    return 0;
}
